
def get_nums(text, len_min=1):
    """
    ===========================================================================
     Description: Return List of Numbers that extracted from the str.
    ===========================================================================
     Arguments:
    ---------------------------------------------------------------------------
        1. text : str 
    ===========================================================================
     Return: list of int
    ===========================================================================
    """
    nums = list()
    cur = str()
    for ch in text:
        if str.isdecimal(ch):
            if cur:
                cur += ch
            else:
                cur = ch
        else:
            if cur:                
                if len(cur)>=len_min:
                    nums.append(int(cur))    
                cur = str()
    if cur:
        if len(cur)>=len_min:
            nums.append(int(cur))
    return nums


def get_path(pre,post):
    """
    ===========================================================================
     Description: Return Full Path of Pre Path and Post file / dir.
    ===========================================================================
     Arguments:
    ---------------------------------------------------------------------------
        1. pre : str (Pre Path).
        2. post : str (File or Dir).
    ===========================================================================
     Return: str (Full Path)
    ===========================================================================
    """
    return '{0}\\{1}'.format(pre,str(post))


